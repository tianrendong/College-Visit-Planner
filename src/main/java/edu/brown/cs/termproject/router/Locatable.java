package edu.brown.cs.termproject.router;

/**
 * Interface for any Locatable object.
 */
public interface Locatable {

  /**
   * Gets the latitude of the object.
   * @return latitude of the object.
   */
  double getLat();

  /**
   * Gets the longitude of the object.
   * @return longitude of the object.
   */
  double getLon();
}


